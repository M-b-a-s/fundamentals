package method;

/** Methods are subtasks within a class
* Methods are used to perform a task
* Methods are also known as functions
* An access modifier is used to specify the access level of a method(public, private, protected)
* If an access modifier is not specified, it is default(only accessible within the same package)
* Non-access modifiers provide additional functionality to a method
* Some non-access modifiers are static, final, abstract, synchronized, native, etc.
* return type is the data type of the value the method returns
* If a method does not return any value, the return type is void
* A method can only retun one value
* Method name should be a verb and follow camelCase naming convention
* Parameters are variables passed to the method
* Arguments are the actual values passed to the method
*/

public class Methods {
    public static void main(String[] args) {
        System.out.println("Hello World");

    }
}
